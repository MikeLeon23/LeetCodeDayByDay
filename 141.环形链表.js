/**
 * Definition for singly-linked list.
 * function ListNode(val) {
 *     this.val = val;
 *     this.next = null;
 * }
 */

/**
 * @param {ListNode} head
 * @return {boolean}
 */

// 方法一：双指针，一个指针每次移动一个节点，一个指针每次移动两个节点，如果存在环，那么这两个指针一定会相遇。
var hasCycle = function (head) {
    if (head === null) {
        return false;
    }
    let l1 = head, l2 = head.next;
    while (l1 != null && l2 != null && l2.next != null) {
        if (l1 === l2) {
            return true;
        }
        l1 = l1.next;
        l2 = l2.next.next;
    }
    return false;
};

// 方法二：哈希表（借助map）
var hasCycle = function (head) {
    var map = new Map();
    while(head){
        if(map.has(head)){
            return true;
        }
        map.set(head, 1);
        head = head.next;
    }
    return false;
};
